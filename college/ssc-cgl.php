<h3>Syllabus : SSC – CGL</h3>
<p></p></center><br>
<strong>A.  General Intelligence &amp; Reasoning :</strong> It would include questions of both verbal and non-verbal type. This component may include questions on analogies, similarities and differences, spatial visualization, spatial orientation, problem solving, analysis, judgement, decision making, visual memory, discrimination, observation, relationship concepts, arithmetical reasoning and figural classification, arithmetic number series, non-verbal series, coding and decoding, statement conclusion, syllogistic reasoning etc.<br>
<strong>B.  General Awareness :</strong> Questions in this component will be aimed at testing the candidates general awareness of the environment around him and its application to society. Questions will also be designed to test knowledge of current events and of such matters of every day observations and experience in their scientific aspect as may be expected of any educated person. The test will also include questions relating to India and its neighbouring countries especially pertaining to sports, History, Culture, Geography, Economic Scene, General Polity, Indian Constitution, scientific Research etc. These Questions will be such that they do not require a special study of any discipline.<br>
<strong>C.  Numerical Aptitute :</strong> The questions will be designed to test the ability of appropriate use of numbers and number sense of the candidate. The scope of the test will be the computation of whole numbers, decimals and fractions and relationships between numbers. It will test sense of order among numbers, ability to translate from one name to another, sense or order of magnitude, estimation or prediction of the outcome of computation, selection of an appropriate operation for the solution of real life problems and knowledge of alternative computation procedures to find answers. The questions would also be based on arithmetical concepts and relationship between numbers and not on complicated arithmetical computation (The standard of the questions will be of 10+2 level).<br>
<strong>D.  English Comprehension :</strong> Candidates’ ability to understand correct English, his basic comprehension and writing ability, etc. would be tested. (The questions in the components A,B &amp; D will be of a level commensurate with the Essential Qualification prescribed for the post viz; graduation).<p></p>
<p><strong>(B). Scheme of Examination for Combined Graduate Level (Tier-II) Examination:</strong><br>
    Tier-II of the Combined Graduate Level Examination -2010will be of Objective Type Multiple Choice and will be conducted over two days during a weekend. It will consist of three different papers/subjects and depending upon the category of posts applied for, the candidate will be required to appear in one, two or three papers, as the case may be.</p>
<p> FOR THE POST OF ASSISTANTS, INSPECTOR OF INCOME-TAX/INSPECTOR (CENTRAL EXCISE,INSPECTOR(PO), INSPECTOR(EXAMINER) SUB INSPECTOR IN CBI, INSPECTOR OF POSTS, ASSISTANT ENFORCEMENT OFFICER, DIVISIONAL ACCOUNTANTS,ACCOUNTANTS, AUDITORS, TAX ASSISTANTS, UDCs, THE EXAMINATION WILL CONSIST OF TWO PAPERS AS UNDER :<br>
</p><center><img src="http://princecollegesikar.com/wp-content/uploads/2018/06/22222-944x1024.jpg" alt="" width="944" height="1024" class="aligncenter size-large wp-image-374" srcset="https://princecollegesikar.com/wp-content/uploads/2018/06/22222-944x1024.jpg 944w, https://princecollegesikar.com/wp-content/uploads/2018/06/22222-277x300.jpg 277w, https://princecollegesikar.com/wp-content/uploads/2018/06/22222-768x833.jpg 768w, https://princecollegesikar.com/wp-content/uploads/2018/06/22222.jpg 1316w" sizes="(max-width: 944px) 100vw, 944px"></center><br>
<b>NOTE  :</b><br>
1.   The Commission will have the full discretion to fix separate minimum qualifying marks in each of the papers and in the aggregate of all the papers separately for each category of candidates (viz. SC/ST/OBC/PH/ExS/General (UR) ). Only those candidates who qualify in all the papers as well as in the aggregate would be eligible to be considered for being called for the Personality Test/ Interview and/or Skill Test.<br>
2.  There will be different set of Questions for the papers on Arithmetic &amp; General Studies for Visually Handicapped (VH) candidates which shall not have any component of Map/Graphs/Statistical Data/ Diagrams/Figures/Geometrical problems/Pie-chart etc. However, components of other papers will be the same as that for general candidates.<br>
<center><p></p>
<h3>SYLLABUS : TIER-II OF THE EXAMINATION</h3>
<p></p></center><br>
<b>Paper-I : </b>Arithmetic Ability : This paper will include questions on problems relating to Number Systems, Computation of Whole Numbers, Decimals and Fractions and relationship between Numbers, Fundamental Arithmetical Operations, Percentage, Ratio and Proportion, Average, Interest, Profit and Loss, Discount, Use of Table and Graphs, Mensuration, Time and Distance, Ratio and Time etc.<br>
<b>Paper-II :</b> English Language &amp; Comprehension :Questions in this components will be designed to test the candidate’s understanding and knowledge of English Language and will be based on error recognition, fill in the blanks (using verbs, preposition, articles etc), Vocabulary, Spellings, Grammar, Sentence Structure, Synonyms, Antonyms, Sentence Completion, Phrases and Idiomatic use of Words, etc. There will be a question on passages and omprehension of passages also. (The standard of the questions will be of 10+2 level).<br>
<b>Paper-III : </b>Commerce/Mathematics/Statistics/Economics for Investigator Grade-II, for Ministry of Statistics &amp; Programme Implementation and Compiler for Registrar General of India, Ministry of Home Affairs. (?)<br>
<b>A.   STATISTICS</b><p></p>
<p> Probability, Probability Distributions, Binomial, Poisson, Normal, Exponential.<br>
    Compilation, classification, tabulation of Statistical Data, Graphical presentation of data.<br>
Measures of central tendency, measures of dispersion, measures of association and contingency, scatter diagram, correlation coefficient, rank correlation coefficient and linear regression analysis ( for two or more variables ) excluding partial correlation coefficients.<br>
Concept of Population, random sample, parameters, statistics, sampling distribution of x properties of estimators and estimation of confidence intervals.<br>
Principles of sampling, simple random sampling, stratified sampling, systematic sampling etc., Sampling and non-sampling errors, type-I and type-II errors.<br>
    Concepts of Hypothesis – Null and alternate, Testing of hypothesis for large samples as well as small samples including Chi-square tests.<br>
Index Numbers, Time series analysis – components of variation and their estimation.</p>
<p><b>B.    ECONOMICS</b></p>
<p><b>GENERAL ECONOMICS</b><br>
1.  Demand and Supply Analysis, including Laws and Interaction of Demand and Supply.<br>
2.  Production Function and Laws of Returns.<br>
3.  Commodity Pricing – Characteristics of various Market Forms and Price Determination under such Market Forms.<br>
4.  Theory of Factor Pricing – Rent, Wage, Interest and Profit.<br>
5.  Theory of Employment – Classical and Neo-classical Approach.<br>
6    Keynesian Theory of Employment – Principles of Effective Demand. Meaning and Importance of Investment, Relation between Saving and Investment, Multiplier Effect and the process of Income Generation, Post Keynesian Development.<br>
7.  Nature and Functions of Money, Value of Money, Fluctuations in the value of Money – Inflation and Deflation, Monetary Policy, Index Number.<br>
8.  International Trade-Free Trade and Protection, Theories of International Trade.<br>
9.  Foreign Exchange – Determination of the rate of Exchange – Purchasing Power Parity theory and Balance of Payment Theory.<br>
10. Public Finance – Nature. Scope and importance of Public Finance.<br>
11. Taxation – Meaning, Classification and Principles of Taxation, Incidence of Taxation.<br>
12. Deficit Financing.<br>
13. Fiscal Policy.</p>
<p><b>INDIAN ECONOMICS AND GENERAL STATISTICS</b></p>
<p>1.   Statistical Investigation – Meaning and Planning of Investigation.<br>
2.  Collection of data and editing of data.<br>
3.  Types of sampling.<br>
4.  Schedule and questionnaire.<br>
5.  Presentation of data – classification, tabulation, etc.<br>
6.  Measures of Central Tendency.<br>
7.  National Income and Accounting – Estimation of National Income, Trends in National Income, Structural changes in the Indian Economy as seen in National Income Data.<br>
8.  Agricultural sector – Agricultural Development during Plan Period, Rural Credit, Agricultural Price Policy, Rural Development Co-operation and Panchayati Raj.<br>
9.  Industrial Policy and Industrial Development.<br>
10. Problems of Economic Development – Indian Planning – Objectives, Techniques and its evolution, Five Year Plans and Role of National Development Council.<br>
11. Profile of Human Resources – Population and Economic Development, Demographic Profile of India, Nature of Population Problem – Poverty, Inequality, Unemployment Problem, Labour Problem, Population Control and Government Policy.<br>
12. New Economic Policy and Welfare Schemes.<br>
13. Indian Public Finance – Indian Revenue, Foreign Aid.<br>
14. Indian Banking and Currency system.</p>
<p><b>C.    MATHEMATICS</b></p>
<p>Algebra : Algebra of sets, relations and functions, Inverse of a function, equivalence relation.The system of complex numbers, De Moivere’s Theorem and its simple applications. Relation between roots and coefficients of a polynomial equation – Evaluation of symmetric function of roots of cubic and biquadratic equation.<br>
Algebra of Matrices: Determinants, Simple properties of determinants, Multiplication of determinants of orders two and three, Singular and non-singular matrices. Inverse of a matrix, Rank of a matrix and application of matrices to the solution of linear equations ( in three unknowns ). Convergence of sequences, and series, tests of convergence of series with positive terms, Ratio, Root and Gauss tests.<br>
Analytic Geometry: Straight lines, Circles, System of circles, parabola, ellipse and hyperbola in standard form and their elementary properties, Classification of curves second degree.<br>
Differential Equation: First order differential equation. Solution of Second and higher order linear differential equations with constant coefficients and simple applications.<br>
Differential and Integral Calculus : Limit, continuity and differentiability of functions, successive differentiation,  derivatives of standard functions, Rolle’s and Mean-value Theorems, Maclaurins and Taylor’s series ( without proof) and their applications, Maxima and Minima of functions of one and two variables. Tangents and Normals, Curvature, Partial differentiation, Euler’s theorem for homogeneous function, Tracing of curves. Standard methods of integration, Riemann’s definition of definite integral, fundamental theorem of integral calculus, quadrature, rectification, volumes and surface area of solids of revolution.<br>
Statistics : Frequency distributions, Measures of central tendency, measures of dispersion, Skewness and Kurtosis, Random variables and distribution function, Discrete distributions, Binomial and Poisson distribution, continuous distributions, Rectangular, Normal and Exponential distributions, Principles of least squares, correlation and regression, Random Sampling, random numbers, Sampling of attributes, Large Sample tests for mean and proportion, Tests of significance based on t, F and Chi-square distributions.</p>
<p><b>D. COMMERCE</b></p>
<p> This paper will cover all the subjects of commerce ordinarily taught at the B.Com. or similar degree courses of Indian Universities. Specifically, it will include the following subjects:<br>
Accountancy : Conceptual framework, Income measurement, Final accounts, Accounting for partnership  firms, Hire-purchase accounting, Corporate accounting ( Issue, forfeiture and re-issue of shares ).<br>
Business Organisation: Business objectives, Business environment, Business entrepreneurship ( including location, choice of form of business and growth strategies ), Business operations including finance, production, marketing and human resource development.<br>
<b>Management:</b> Concept of management, Planning, Organising, Leading and Controlling.<br>
Micro-economics: Price-mechanism, Theory of consumer behaviour, Elasticity of demand, Production function, Theory of costs, Market structures, Price-determination under perfect competition and monopoly.<br>
Indian Economics: Issues involved in planning for economic development, Sectoral analysis of Indian economy including agriculture, industry and foreign trade.<br>
