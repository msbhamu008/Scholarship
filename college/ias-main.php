<h3>Syllabus : IAS (Mains)</h3>
<p></p></center><br>
The Main examination of UPSC is designed to test a candidate’s academic expertise and ability to present his/her knowledge in a consistent manner. The UPSC Mains exam intends to assess the overall intellectual traits and understanding of concept by the candidates. The Syllabus for UPSC Mains was revised in 2015.<br>
The Civil Services Main examination is designed to test the academic talent of the aspirant, also his/her ability to present his/ her knowledge in a clear and coherent manner. The written examination will consist of the following papers:<p></p>
<p><strong>Paper I – Modern Indian language – 300 Marks </strong><br>
Qualifying nature: Marks not counted, Passing mandatory<br>
Comprehension of given passages<br>
Precis Writing<br>
Usage and Vocabulary<br>
Short Essay<br>
Translation from English to the Indian language and vice-versa<br>
Note 1: The Papers on Indian Languages and English will be of Matriculation or equivalent standard and will be of qualifying nature only. The marks obtained in these papers will not be counted for ranking.<br>
Note 2: The candidates will have to answer the English and Indian Languages papers in English and the respective Indian language (except where translation is involved).</p>
<p><strong>Paper II – English – 300 marks </strong><br>
Qualifying nature : Marks not counted, passing mandatory</p>
<p>The aim of the paper is to test the candidates’ ability to read and understand serious discursive prose, and to express his ideas clearly and correctly, in English and Indian Language concerned.<br>
The pattern of questions would be broadly as follows : –<br>
Comprehension of given passages<br>
Precis Writing<br>
Usage and Vocabulary<br>
Short Essay</p>
<p><b> Paper III – Essay – 250 Marks </b><br>
To be written in the medium or language of the candidate’s choice<br>
Candidates will be required to write an essay on a specific topic<br>
The choice of subjects will be given<br>
They will be expected to keep closely to the subject of the essay to arrange their ideas in orderly fashion, and to write concisely<br>
Credit will be given for effective and exact expression</p>
<p><strong>Paper IV – General Studies – I 250 Marks </strong><br>
(Indian Heritage and Culture, History and Geography of the World and Society)<br>
Indian culture will cover the salient aspects of Art Forms, Literature and Architecture from ancient to modern times.<br>
Modern Indian history from about the middle of the eighteenth century until the present- significant events, personalities, issues<br>
The Freedom Struggle – its various stages and important contributors /contributions from different parts of the country<br>
Post-independence consolidation and reorganization within the country History of the world will include events from 18th century such as industrial revolution, world wars, redrawal of national boundaries, colonization, decolonization, political philosophies like communism, capitalism, socialism etc.- their forms and effect on the society<br>
Salient features of Indian Society, Diversity of India<br>
Role of women and women’s organization, population and associated issues, poverty and developmental issues, urbanization, their problems and their remedies<br>
Effects of globalization on Indian society<br>
Social empowerment, communalism, regionalism &amp; secularism<br>
Salient features of world’s physical geography<br>
Distribution of key natural resources across the world (including South Asia and the Indian sub-continent); factors responsible for the location of primary, secondary, and tertiary sector industries in various parts of the world (including India)<br>
Important Geophysical phenomena such as earthquakes, Tsunami, Volcanic activity, cyclone etc., geographical features and their location- changes in critical geographical features (including water-bodies and ice-caps) and in flora and fauna and the effects of such changes.</p>
<p><strong>Paper V – General Studies II – 250 Marks </strong><br>
(Governance, Constitution, Polity, Social Justice and International relations)<br>
Indian Constitution- historical underpinnings, evolution, features, amendments, significant provisions and basic structure.<br>
Functions and responsibilities of the Union and the States, issues and challenges pertaining to the federal structure, devolution of powers and finances up to local levels and challenges therein.<br>
Separation of powers between various organs dispute redressal mechanisms and institutions.<br>
Comparison of the Indian constitutional scheme with that of other countries<br>
Parliament and State Legislatures – structure, functioning, conduct of business, powers &amp; privileges and issues arising out of these<br>
Structure, organization and functioning of the Executive and the Judiciary Ministries and Departments of the Government; pressure groups and formal/informal associations and their role in the Polity<br>
Salient features of the Representation of People’s Act<br>
Appointment to various Constitutional posts, powers, functions and responsibilities of various Constitutional Bodies<br>
Statutory, regulatory and various quasi-judicial bodies<br>
Government policies and interventions for development in various sectors and issues arising out of their design and implementation<br>
Development processes and the development industry the role of NGOs, SHGs, various groups and associations, donors, charities, institutional and other stakeholders<br>
Welfare schemes for vulnerable sections of the population by the Centre and States and the performance of these schemes; mechanisms, laws, institutions and Bodies constituted for the protection and betterment of these vulnerable sections<br>
Issues relating to development and management of Social Sector/Services relating to Health, Education, Human Resources<br>
Issues relating to poverty and hunger<br>
Important aspects of governance, transparency and accountability, e-governance- applications, models, successes, limitations, and potential; citizens charters, transparency &amp; accountability and institutional and other measures<br>
Role of civil services in a democracy<br>
India and its neighborhood- relations<br>
Bilateral, regional and global groupings and agreements involving India and/or affecting India’s interests<br>
Effect of policies and politics of developed and developing countries on India’s interests, Indian diaspora.<br>
Important International institutions, agencies and fora, their structure, mandate.</p>
<p><strong>Paper VI – General Studies III – 250 Marks</strong><br>
(Technology, Economic Development, Bio-diversity, Environment, Security and Disaster Management)<br>
Development, Bio diversity, Environment, Security and Disaster Management.<br>
Indian Economy and issues relating to planning, mobilization of resources, growth, development and employment.<br>
Inclusive growth and issues arising from it.<br>
Government Budgeting.<br>
Major crops cropping patterns in various parts of the country, different types of irrigation and irrigation systems storage, transport and marketing of agricultural produce and issues and related constraints; e-technology in the aid of farmers<br>
Issues related to direct and indirect farm subsidies and minimum support prices; Public Distribution System objectives, functioning, limitations, revamping; issues of buffer stocks and food security; Technology missions;economics of animal-rearing.<br>
Food processing and related industries in India- scope and significance, location, upstream and downstream requirements, supply chain management.<br>
Land reforms in India.<br>
Effects of liberalization on the economy, changes in industrial policy and their effects on industrial growth.<br>
Infrastructure: Energy, Ports, Roads, Airports, Railways etc.<br>
Investment models.<br>
Science and Technology- developments and their applications and effects in everyday life Achievements of Indians in science &amp; technology;indigenization of technology and developing new technology<br>
Awareness in the fields of IT, Space, Computers, robotics, nano-technology, bio-technology and issues relating to intellectual property rights.<br>
Conservation, environmental pollution and degradation, environmental impact assessment<br>
Disaster and disaster management.<br>
Linkages between development and spread of extremism.<br>
Role of external state and non-state actors in creating challenges to internal security.<br>
Challenges to internal security through communication networks, role of media and social networking sites in internal security challenges, basics of cyber security; money-laundering and its prevention<br>
Security challenges and their management in border areas; linkages of organized crime with terrorism<br>
Various Security forces and agencies and their mandate</p>
<p><strong>Paper VII – General Studies IV – 250 Marks </strong><br>
(Ethics, Integrity and Aptitude)<br>
This paper will include questions to test the candidates’ attitude and approach to issues relating to integrity, probity in public life and his problem solving approach to various issues and conflicts faced by him in dealing with society. Questions may utilise the case study approach to determine these aspects. The following broad areas will be covered.<br>
Ethics and Human Interface: Essence, determinants and consequences of Ethics in human actions; dimensions of ethics; ethics in private and public relationships.<br>
Human Values – lessons from the lives and teachings of great leaders, reformers and administrators; role of family, society and educational institutions in inculcating values.<br>
Attitude: content, structure, function; its influence and relation with thought and behaviour; moral and political attitudes; social influence and persuasion.<br>
Aptitude and foundational values for Civil Service , integrity, impartiality and non-partisanship, objectivity, dedication to public service, empathy, tolerance and compassion towards the weaker-sections.<br>
Emotional intelligence-concepts, and their utilities and application in administration and governance.<br>
Contributions of moral thinkers and philosophers from India and world.<br>
Public/Civil service values and Ethics in Public administration: Status and problems; ethical concerns and dilemmas in government and private institutions; laws, rules, regulations and conscience as sources of ethical guidance; accountability and ethical governance; strengthening of ethical and moral values in governance; ethical issues in international relations and funding; corporate governance.<br>
Probity in Governance: Concept of public service; Philosophical basis of governance and probity; Information sharing and transparency in government, Right to Information, Codes of Ethics, Codes of Conduct, Citizen’s<br>
Charters, Work culture, Quality of service delivery, Utilization of public funds, challenges of corruption<br>
Case Studies on above issues.</p>
<p><strong>Paper VIII – Optional Subject – Paper I – 250 Marks </strong><br>
Candidates may choose any ONE optional subject from amongst the list of subjects given below.</p>
<p><strong>Paper IX – Optional Subject – Paper II -250 Marks </strong><br>
Candidates may choose any ONE optional subject from amongst the list of subjects given below.</p>
<p><strong>Interview/Personality Test – 275 Marks </strong><br>
Candidate can give preference of the language in which they may like to be interviewed. UPSC will make arrangement for the translators.<br>
<b>Note: </b>Marks obtained by the candidates for all papers EXCEPT PAPER A &amp; B will be counted for merit ranking. However, the Commission will have the discretion to fix qualifying marks in any or all papers of the examination Aspirants can log on to UPSC’s official website (www.upsc.gov.in) to see the detailed notification.</p>
