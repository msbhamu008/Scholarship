<h3>Syllabus : SSC SI &amp; ASI Exam</h3>
<p></p></center><br>
SSC SI &amp; ASI (Delhi Police, CAPF, CISF) Exam Syllabus: Staff Selection Commission (SSC) Sub-Inspectors in Delhi Police, CAPFs and Assistant Sub-Inspector in CISF Examination syllabus details are given below….<p></p>
<p><b>Paper – I :</b><br>
<b>A.   General Intelligence &amp; Reasoning:</b> It would include questions of both verbal and non verbal type. This componenet may include questions on analogies, similarities and differences, space visualization, spatial orientation, problem solving, analysis, judgment, decision making, visual memory, discrimination, observation, relationship concepts, arithmetical reasoning and figural classification, arithmetic number series, non-verbal series, coding and decoding, statement conclusion, sullogistic reasoning etc. The topics are, Semantic Analogy, Symbolic/ Number Analogy, figural analogy, Semantic classification, symbolic/ Number classification, figural classification, semantic series, number series, figural series, problem solving, word building, coding &amp; de-coding, numerical operations, symbolic operations, trends, space orientation, space visualization, venn diagrams, drawing inferences, punched hole/ pattern-folding &amp; unfolding, figural pattern-folding and completion, indexing address matching, date &amp; city matching classification, embedded figures, critical thinking , emotional intelligence, social intelligence, other sub-topics if any.<br>
<b>B.   General Awareness :</b> Questions in this component will be aimed at testing the candidates general awareness of the environment around him and its application to society. Questions will also be designed to test knowledge of current events and of such matters of every day observations and experience in their scientific aspect as may be expected of any educated person. The test will also include questions relating to India and its neighboring countries especially pertaining to History, Culture, Geography, Economic Scene, General Polity, Indian Constitution, scientific Research etc.<br>
<b>C.   Quantitative Aptitude :</b> The questions will be designed to test the ability of appropriate use of numbers and number sense of the candidate. The scope of the test will be computation of whole numbers, decimals, fractions and relationships between numbers, Percentage, Ratio and Proportion, Square roots, Averages, Interest, Profit &amp; Loss, Discount, Partnership Business, Mixture and Allegation, Time and distance, Time &amp; work, Basic algebraic identities of School Algebra and Elementary surds, Graphs of Linear Equations, Triangle and its various kinds of centres, Congruence and similarity of triangles, Circle and its chords, tangents, angles subtended by chords of a circle, common tangents to two or more circles, Triangle , Quadrilaterals, Regular Polygons, Circle, Right Prism, Right Circular Cone, Right Circular Cylinder, Sphere, Hemispheres, Rectangular Parallelepiped, Regular Right Pyramid with triangular or square base, Trigonometric ratio, Degree and Radian Measures, Standard Identities, Complementary angles, Heights and Distances, Histogram, Frequency polygon, Bar diagram &amp; Pie chart.<br>
<b>D.   English Comprehension :</b> Candidates ability to understand correct English, his basic comprehension and writing ability, etc. would be tested.<br>
<b>Paper – II :</b><br>
    English Language &amp; Comprehension: Questions in this components will be designed to test the candidate‟s understanding and knowledge of English Language and will be based on error recognition, filling in the blanks (using verbs, preposition, articles etc), Vocabulary, Spellings, Grammar, Sentence Structure, Synonyms, Antonyms, Sentence Completion, Phrases and Idiomatic use of Words, comprehension etc.</p>
