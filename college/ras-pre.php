<h3>Syllabus : RAS – Preliminary</h3>
<p></p></center><br>
<strong>Scheme of Examination:</strong><p></p>
<p>The Preliminary Examination will consist of one paper on the subject specified below, which will be of objective type and carry a maximum of 200 marks.<br>
The examination is meant to serve as a screening test only. The Standard of the paper will be that of a Bachelor’s Degree Level. The marks obtained in the Preliminary Examination by the candidates, who are declared qualified for admission to Main Examination will not be counted for determining their final order of merit.</p>
<p><strong>Paper Subject Max Marks Time</strong><br>
I General Knowledge and General Science 200 3 Hours</p>
<p><b>Note-</b> 1. There will be 150 questions of multiple choice (Objective Type), carrying equal marks.<br>
2. There will be negative marking.1/3 mark will be deducted for each wrong answer.</p>
