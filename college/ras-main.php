                                                         <p></p><center><strong><p></p>
<h2>Syllabus : RAS (Mains)</h2>
</strong><p><strong></strong></p></center><br>
<b>Scheme of Examination :</b><br>
(a) The number of candidates to be admitted to the Main Examination will be 15 times the total approximate number of vacancies (category wise) to be filled in the year in the various Services and posts, but in the said range all those candidates who secure the same percentage of marks as may be fixed by the Commission for any lower range will be admitted to the Main Examination.<br>
(b) The written examination will consist of the following four papers which will be descriptive /analytical. A candidate must take all the papers listed below which will also consist of question paper of brief, medium, long answer and descriptive type question. The standard of General Hindi and General English will be that of Sr. Secondary Level. The time allowed for each paper shall be 3 hours.<p></p>
<p></p><center><img src="images/ras.jpg">
    <br>
<b>Paper – I</b></center><br>
<b> General Knowledge and General Studies</b><br>
Unit I – HISTORY<br>
Part A – History, Art, Culture, Literature, Tradition and Heritage of Rajasthan<br>
Major landmarks in the History of Rajasthan from Pre-historic time to close of 18th Century, Important dynasties, their administrative and revenue system.<br>
Salient events of 19th &amp; 20th centuries: Peasant &amp; Tribal Movements. Political Awakening, Freedom Movement and Integration.<br>
Heritage of Rajasthan: Performing &amp; fine Art, Handicraft and Architecture; Fairs, Festivals, Folk Music and Folk Dance<br>
Important works of Rajasthani Literature and Dialects of Rajasthan.<br>
Saints , Lok Devtas and eminent personalities of Rajasthan<br>
Part B – Indian History &amp; Culture<br>
Indian heritage: Fine Art, Performing Art, Architecture &amp; Literature from Indus Civilization to British Era.<br>
Religious Movements and religious philosophy in Ancient and Medieval India.<br>
History of Modern India from beginning of 19th Century to 1965 AD: Significant events, personalities and issues.<br>
Indian National Movement- Its various stages &amp; streams, important contributors and contribution from different parts of the country.<br>
Socio-religious Reform Movements in 19th and 20th Century.<br>
Post Independence consolidation and reorganisation – Accession of princely states &amp; Linguistic reorganisation of the states.<br>
Part C – History of Modern World (up to 1950AD)<br>
Renaissance and Reformation.<br>
Enlightenment and Industrial Revolution.<br>
Imperialism and colonialism in Asia and Africa.<br>
Impact of World Wars.<p></p>
<p><b>Unit II- ECONOMICS</b><br>
Part A- Indian Economy<br>
Major Sectors of Economy: Agriculture, Industry &amp; Service- Current Status, Issues and Initiatives.<br>
Banking: Concept of Money supply &amp; High Powered Money. Role and Functions of Central Bank &amp; Commercial Banks, issues of NPA, Financial Inclusion. Monetary Policy- Concept, objectives &amp; Instruments.<br>
Public Finance: Tax reforms in India- Direct &amp; Indirect, subsidies- Cash Transfer and other related issues. Recent Fiscal Policy of India.<br>
Recent Trends in Indian Economy: Role of Foreign Capital, MNCs, PDS, FDI, Exim Policy, 12th Finance Commission, Poverty alleviation schemes.<br>
Part B- World Economy<br>
Global Economic issues and trends: Role of World Bank, IMF &amp; WTO.<br>
Concept of Developing, Emerging and Developed countries.<br>
India in global Scenario.<br>
Part C- Economy of Rajasthan<br>
Agriculture, Horticulture, Forestry, Dairy and Animal husbandry with special reference to Rajasthan.<br>
Industrial Sector- Growth and recent trends.<br>
Growth, Development &amp; Planning with special reference to Rajasthan.<br>
Recent development and issues in service sector of Rajasthan.<br>
Major Development Projects of Rajasthan- their objectives and impact.<br>
Public Private Partnership Model for Economic Transformation in Rajasthan.<br>
Demographic Scenario of the State and its impact on Rajasthan Economy.<br>
<b>Unit III</b><br>
Sociology, Management, Accounting &amp; Auditing<br>
<b>Part A- Sociology</b><br>
Development of Sociological Thought in India<br>
Social Values<br>
Caste Class &amp; Occupation<br>
Sanskritization<br>
Varna, Ashram, Purusharth and Sanskar Vyavastha<br>
Secularism<br>
Issues and Problems of Society.<br>
Tribal community of Rajasthan: Bhil, Mina (Meena) and Garasia.</p>
<p><strong>Part B- Management</strong><br>
Management – Scope, concept, functions of Management – Planning, Organizing, Staffing, Direction, Coordination and Control, Decision-Making: concept, process and techniques.<br>
Modern concept of Marketing, Marketing Mix – Product, Price, Place and Promotion<br>
Objective, concept of maximization of wealth, Sources of Finance – Short and Long term, Capital Structure, Cost of Capital<br>
Concept and Main theories of Leadership and Motivation, Communication, Basics of recruitment, selection, induction, training &amp; development and appraisal system</p>
<p><strong>Part C- Accounting &amp; Auditing</strong><br>
Techniques of analysis of Financial statements, Basics of Working Capital Management, Responsibility and Social Accounting<br>
Meaning &amp; Objectives of Auditing, Internal Control, Social, Performance and Efficiency Audit.<br>
Basics of different types of Budgeting, Budgetary control<br>
</p><center><strong>Paper – II</strong></center><br>
General Knowledge and General Studies<br>
<strong>Unit I- Administrative Ethics</strong><br>
Ethics and Human Values : Lesson from lives and teachings of great leaders, reformers and administrators. Role of family, society and educational institutions in inculcating values.<br>
Ethical concept-Rit and Rin, concept of Duties, concept of Good and Virtues.<br>
Ethics in private and public relationships- Behaviour, Moral and Political attitudes of administrators – Philosophical basis of Integrity.<br>
Ethics of Bhagavad Geeta and its role in Administration.<br>
Gandhian Ethics.<br>
Contribution of Moral Thinkers and Philosophers from India.<br>
Psycho-Stress Management.<br>
Case Studies.<br>
Emotional Intelligence – Concepts and their Utilities.<br>
<strong>Unit II- General Science &amp; Technology</strong><br>
Nanotechnology :- Concept and its application; Nano Mission of India.<br>
Nuclear technology :- Basic concept, radioactivity and its applications, different types of nuclear reactors, civilian and military uses. Institutional structure for development of nuclear technology in India.<br>
Telecommunication :- Basic concept, Telecom application for socio-economic development of masses, Indian telecom industry with brief history, National Telecom Policy and TRAI (Telecom Regulatory Authority of India)<br>
Electromagnetic waves, communication systems, basics of computers, uses of information technology, e-governance and e-commerce in administration.<br>
Defence :- Types of missiles with reference to the India Missile Programme; Various chemical and biological weapons ; Role of DRDO in various fields (other than weapons).<br>
States of Matter<br>
Allotropes of carbon.<br>
pH Scale and importance of pH in daily life.<br>
Corrosion and its prevention<br>
Catalyst<br>
Soap and Detergents – Cleansing action of soap.<br>
Polymers and their uses<br>
General awareness of digestive, respiratory, circulatory, excretory, coordination and reproductive system of Humans<br>
Application of Biotechnology and concerned Ethical and Intellectual Property Right Issues.<br>
Food and Human Health : General Awareness of Balanced and Unbalanced Food, Malnutrition, Intoxicants, Blood, Blood Group and Immunity (Antigen, Antibodies), Blood Transfusion, Immunization &amp; Vaccination.<br>
Human diseases : Communicable &amp; Non Communicable Diseases, Acute and Chronic Diseases; Causes and Prevention of Infectious, Genetic and Lifestyle Disease.<br>
Water quality and water purification.<br>
Public Health Initiatives with special reference to the State of Rajasthan.<br>
Contribution of Indian Scientists in Science and Technology.<br>
Ecosystem : Structure and Function.<br>
Atmosphere : Composition and Basic nutrient cycling (Nitrogen, Carbon and Water Cycles)<br>
Climate Change; Renewable and Non-Renewable Energy.<br>
Environmental Pollution and Degradation; Waste Management.<br>
Biodiversity and its conservation with special reference to the state of Rajasthan.<br>
Water Conservation with special reference to the Traditional Systems in the State of Rajasthan.<br>
Agriculture, Horticulture, Forestry, Dairy and Animal Husbandry with special reference to Rajasthan.<p></p>
<p><strong>Unit III- Earth Science (Geography &amp; Geology)</strong><br>
<b>Part A- World</b><br>
Broad Physical Feature: Mountains, Plateaus, Plains, Lakes and Glaciers.<br>
Earthquakes and Volcanoes: Types, distribution and their impact<br>
Earth and its Geological time scale.<br>
Current Geopolitical Problems.<br>
<b>Part B- India</b><br>
Broad Physical Features: Mountains, Plateaus, Plains, Lakes and Glaciers.<br>
Major Physiographic divisions of India.<br>
Climate- Origin of Monsoon, Seasonal Climatic conditions, Distribution of rainfall and climatic regions,<br>
<b>Natural Resources:</b> (a)   Water, Forest, Soil     (b)    Rocks &amp; Mineral: Types and their uses<br>
Population: Growth, Distribution and Density, Sex-ratio, Literacy, urban and Rural Population.<br>
<b>Part C- Rajasthan</b><br>
Broad Physical Features: Mountains, Plateaus, Plains, Rivers and Lakes.<br>
Major Physiographic Regions.<br>
Natural Vegetation and Climate.<br>
Livestock, Wildlife and its Conversation.<br>
Agriculture – Major Crops.<br>
<b>Mineral Resources : </b><br>
    (i) Metallic Minerals- Types, distribution and Industrial uses and their conservation.<br>
    (ii) Non- Metallic Minerals- Types, distribution and Industrial uses and their conservation.<br>
Energy Resources: Conventional and Non-conventional<br>
Population and Tribes.<br>
</p><center><strong>Paper-III : General Knowledge and General Studies</strong></center><br>
Unit I- Indian Political System, World Politics and Current Affairs<br>
Indian Constitution: Framing, Features, Amendments, Basic Structure.<br>
Ideological Contents: Preamble, Fundamental Rights, Directive Principles of State Policy, Fundamental Duties.<br>
Institutional framework- I: Parliamentary System, President, Prime Minister and Council of Ministers, Parliament.<br>
Institutional Framework- II: Federalism, Centre-State relations, Supreme Court, High Courts, Judicial Review, Judicial Activism.<br>
Institutional Framework- III: Election Commission of India, Comptroller and Auditor General, Union Public Service Commission, NITI Aayog, Central Vigilance Commission, Central Information Commission, National Human Rights Commission<br>
Political Dynamics: Role of Cast, Religion, Class, Ethnicity, Language and Gender in Indian Politics, Political Parties and Electoral Behaviour, Civil Society and Political Movement, Issues related to National Integrity and Security. Potential areas of Socio-Political conflicts<br>
State Politics of Rajasthan: Party System, Political Demography, Different phases of Political competition in Rajasthan, Panchayati Raj and Urban self Government Institutions<br>
Emerging World Order in the post Cold War era, USA’s hegemony and its resistance, UN and Regional Organizations, International Terrorism and Environmental issues.<br>
Foreign Policy of India: Evolution, Determinants, India’s relations with USA, China, Russia and European Union, India’s role in UN, NAM, BRICS, G- 20, G- 77 and SAARC.<br>
Geo-political and Strategic development in South Asia, South East Asia and West Asia and their impact on India.<br>
Current Affairs: Current events, persons and places of Rajasthan, National and international importance, recent activities related to games and sports.<p></p>
<p><b>Unit II- Concepts, Issues and Dynamics of Public Administration and Management</b><br>
Administration and management: Meaning, nature and significance. Its role in developed &amp; developing societies. Evolution of Public Administration as a discipline, new public administration, Theories of public administration.<br>
Concepts of power, authority, legitimacy, responsibility and delegation.<br>
Principles of organization: Hierarchy, Span of control and unity of command<br>
